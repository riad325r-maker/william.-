// اللغة الحالية
let currentLanguage = localStorage.getItem('language') || 'ar';

// دالة تغيير اللغة
function toggleLanguage() {
    currentLanguage = currentLanguage === 'ar' ? 'en' : 'ar';
    localStorage.setItem('language', currentLanguage);
    updatePageLanguage();
    updateDirection();
    
    // تحديث الصفحة إذا كانت في صفحة المشاهدة
    if (window.location.pathname.includes('watch.html')) {
        loadWords();
        loadSentences();
        loadQuiz();
        loadSpeakSection();
    }
}

// تحديث اتجاه الصفحة
function updateDirection() {
    const html = document.documentElement;
    if (currentLanguage === 'ar') {
        html.setAttribute('dir', 'rtl');
        html.setAttribute('lang', 'ar');
    } else {
        html.setAttribute('dir', 'ltr');
        html.setAttribute('lang', 'en');
    }
}

// تحديث كل النصوص في الصفحة
function updatePageLanguage() {
    // تحديث زر اللغة
    const langBtn = document.getElementById('currentLang');
    if (langBtn) {
        langBtn.textContent = currentLanguage === 'ar' ? 'English' : 'العربية';
    }
    
    // تحديث كل العناصر اللي فيها data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        const translation = getTranslation(key);
        if (translation) {
            element.textContent = translation;
        }
    });
    
    // تحديث الـ placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        const translation = getTranslation(key);
        if (translation) {
            element.placeholder = translation;
        }
    });
}

// جلب الترجمة من الكائن
function getTranslation(key) {
    const keys = key.split('.');
    let value = translations[currentLanguage];
    
    for (const k of keys) {
        if (value && value[k]) {
            value = value[k];
        } else {
            console.warn(`Translation missing for key: ${key}`);
            return key;
        }
    }
    
    return value;
}

// دالة مساعدة للترجمة السريعة
function __(key) {
    return getTranslation(key);
}

// تشغيل الترجمة أول ما الصفحة تتحمل
document.addEventListener('DOMContentLoaded', () => {
    updateDirection();
    updatePageLanguage();
});